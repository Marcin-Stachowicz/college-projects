#include <stdio.h>



int main() {
	
	int a,b,c;


	for(a = 0; a < 10; a++) {
	
	
		printf("a is %d\n", a);
	}
	
	
	printf("\n");
	
	
	while(b < 10) {
		
		
		printf("b is %d\n", b);
		b++;
	}	
	
	
	printf("\n");
	
	
	do {
	
		
		printf("c is %d\n", c);
		c++;
	
	
	} while(c < 10);
	

	return 0;
}


